export let constGalleryData = function() {
    // 在Gallery界面初始化时调用
    // 1. 加载存档， 2. 根据存档，构建出Gallery需要的[]数据结构
    var data = {
        max: null,
        nameList: [],
        mottoList: [],
        descList: [],
        zoomList: [],
        picList: [],
        eggList: [],
    };

    var globalData = LoadGameData('GlobalData');
    var CC = CharacterConfig;

    var known = globalData.known; // array，初始为李无奇金老板：1，2
    data.max = known.length;

    for (let i = 0; i < known.length; i++) {
        console.log('constGallery, i = ' + i);
        let charSerial = known[i]; //从Known列表中得到人物编号
        let charObj = CC[charSerial]; //从CC中取得对应人物object

        console.log('constGallery, charSerial = ' + charSerial + ' charObj = ' + charObj);
        data.nameList.push(charObj.name);
        data.mottoList.push(charObj.motto);
        data.descList.push(charObj.desc);
        data.zoomList.push(charObj.zoom);
        data.picList.push(charObj.pic);

        let eggObj = { egg1: {}, egg2: {}, egg3: {}, };
        eggObj.egg1 = charObj.egg1;
        eggObj.egg2 = charObj.egg2;
        eggObj.egg3 = charObj.egg3;

        eggObj.egg1.state = globalData.eggs[charSerial][1];
        eggObj.egg2.state = globalData.eggs[charSerial][2];
        eggObj.egg3.state = globalData.eggs[charSerial][3];

        data.eggList.push(eggObj);

        // GameData中，彩蛋的记录
        //彩蛋拥有情况 0未获得 1获得 2已读 NULL不可攻略;
        //chance：在gallery界面显示的，剩余可选彩蛋次数
        // const _eggs = [
        //     [{ chance: 1 }, 0, 0, 0],
        //     [{ chance: 1 }, 0, 0, 0],
        //     [{ chance: 1 }, 0, 0, 0],
        //     [{ chance: 1 }, 0, 0, 0],
        //     [{ chance: 1 }, 0, 0, 0],
        //     [{ chance: 1 }, 0, 0, 0],
        //     [{ chance: 1 }, 0, 0, 0],
        //     [{ chance: 1 }, 0, 0, 0], null, null, null, null
        // ];
    };
    return data;
};

export let constAttackList = function() {
    var data = [];
    var CC = CharacterConfig;

    for (let i = 0; i < CC.length; i++) {
        data.push(CC[i].attSerial);
    };
    console.log('调用了constAttList，值：' + data[0] + '  ' + data[1]);
    return data;
};


export let constEggChoose = function(n) {
    var data = { egg1: { title: '', serial: null, }, egg2: { title: '', serial: null, }, egg3: { title: '', serial: null, } };
    var CCN = CharacterConfig[n];

    data.egg1.title = CCN.egg1.title;
    data.egg2.title = CCN.egg2.title;
    data.egg3.title = CCN.egg3.title;

    data.egg1.serial = CCN.egg1.serial;
    data.egg2.serial = CCN.egg2.serial;
    data.egg3.serial = CCN.egg3.serial;




    return data;
};